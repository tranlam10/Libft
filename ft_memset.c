#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*tmp;

	i = 0;
	tmp = (char *)b;
	while (len)
	{
		tmp[i] = c;
		i++;
		len--;
	}
	return (b);
}
